<!-- karakteristik procedural programming 
        1. instruksi dilakukan langkah demi langkah
        2. memecah program menjadi bagian-bagian kecil
        3. biasany gaya pemrograman seperti ini disebut prosedur, subroutine atau function
        4. procedural pemrogramming juga disebut linear atau dibaca dari Top-to-Bottom
        5. daftar bahasa yang menggunakan procedural programming yaitu C, PHP, Javascript dan sebagainya 
        
    Kelebihannya
        1. to the point
        2. simplicity dan kemudahan implementasi untuk compiler dan interpreter
        3. mudah dibaca dan ditelusuri
        4. membutuhkan leih sedikit memory daripada oop-->

<!-- OOP atau Object Oriented Programming 
        1. Menyusun semua kode program dan struktur data kedalam objek
        2. Objek merupakan unit dasar dari pembentuk program
        3. didalam objek kita bisa menyimpan data dan perilaku yang disebut property dan method
        4. Objek bisa saling berinteraksi
        5. bahasa yang menggunakan paradigma oop java, ruby, python, javascript, php 
    
     Kelebihannya
        1. bisa direpresentasikan ke dunia nyata
        2. bisa melakukan enskapsulasi dan abstraksi data
        3. reusability/ bisa digunakan ulang
        4. skalabilitas dan ekstensibilitas
        5. kemudahan pengelolaan
        6. memudahkan kolaborasi
        7. konsep ini banyak digunakan oleh framework php -->


