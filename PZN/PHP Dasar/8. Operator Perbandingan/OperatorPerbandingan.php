<?php

// Operator perbandingan, digunakan untuk membandingkan 2 value
// hasil dari operator perbandingan adalah boolean
// true jika benar dan false jika perbandingan salah

// Operator sama dengan $a == $b                = true jika $a sama dengan $b, walaupun tipe data berbeda

// Operator tidak sama dengan $a != $b          = true jika $a tidak sama dengan $b, walaupun tipe data berbeda
// Operator tidak sama dengan $a <> $b          = true jika $a tidak sama dengan $b, walaupun tipe data berbeda

// Operator Identik $a === $b                   = true jika $a sama dengan $b dan memiliki tipe data yang sama
// Operator Tidak Identik $a !== $b             = true jika $a sama dengan $b, walaupun tidak memiliki tipe data yang sama

// Operator Kurang Dari $a < $b                 = true jika $a kurang dari $b
// Operator kurang dari sama dengan $a <= $b    = true jika $a kurang dari sama dengan $b

// Operator lebih dari  $a > $b                 = true jika $a lebih dari $b
// Operator lebih dari sama dengan $a >= $b     = true jika $a lebih dari sama dengan $b